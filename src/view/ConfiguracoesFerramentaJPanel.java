    public void preencherCaminhoPlanoRisco(int idProjetoSelecionado) {

        if (idProjetoSelecionado == 0) {
            caminhoPlanoRiscoJTextField.setText("Nenhum projeto selecionado.");
        } else {
            try {
                Projeto projeto = new Projeto();
                projeto = projetoPlanoRiscoFacade.getProjetoWhereId(idProjetoSelecionado);
                if (projeto.getCaminhoPlanoDeRisco() == null) {
                    caminhoPlanoRiscoJTextField.setText("Nenhum diretório padrão de importação para o projeto selecionado.");
                } else {
                    caminhoPlanoRiscoJTextField.setText(projeto.getCaminhoPlanoDeRisco());
                }
            } catch (NullPointerException e) {

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }

