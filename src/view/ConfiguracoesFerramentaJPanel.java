    private void selecionarDiretorioPoliticaOrganizacionalJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarDiretorioPoliticaOrganizacionalJButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int retorno = fileChooser.showOpenDialog(null);
        
        if (retorno == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            String path; 
            
            path = (file.getAbsolutePath()).toString();
            
            
            try{
                Politicaorganizacional politicaOrg = new Politicaorganizacional();
                politicaOrg = politicaOrgFacade.getPolitica();

                politicaOrg.setCaminhoArquivo(path);
                try {
                    politicaOrgFacade.setPolitica(politicaOrg);
                } catch (Exception ex) {
                    Logger.getLogger(OrganizacionalPoliticaOrganizacionalJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }

                JOptionPane.showMessageDialog(null, "Caminho configurado com sucesso.");
                preencherCaminhoPoliticaOrganizacional();
            }catch(Exception e){
                System.err.printf("Erro na configuração do caminho: %s. \n", e.getMessage());
            }
        }
    }//GEN-LAST:event_selecionarDiretorioPoliticaOrganizacionalJButtonActionPerformed

    private void selecionarDiretorioPlanoRiscoJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarDiretorioPlanoRiscoJButtonActionPerformed
        if (idProjetoSelecionado == 0) {
            JOptionPane.showMessageDialog(this, "Não há projeto selecionado.");
        } else {
            Projeto projeto = new Projeto();
            projeto = projetoPlanoRiscoFacade.getProjetoWhereId(idProjetoSelecionado);

            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

            if (projeto.getCaminhoPlanoDeRisco() != null) {
                File arquivo = new File(projeto.getCaminhoPlanoDeRisco());
                fileChooser.setCurrentDirectory(arquivo);
            }

            int retorno = fileChooser.showOpenDialog(null);

            if (retorno == JFileChooser.APPROVE_OPTION) {
                File file = fileChooser.getSelectedFile();
                String path;

                path = (file.getAbsolutePath()).toString();

                try {
                    projeto.setCaminhoPlanoDeRisco(path);
                    try {
                        projetoPlanoRiscoFacade.alterarPlanoRisco(projeto);
                    } catch (Exception ex) {
                        Logger.getLogger(OrganizacionalPoliticaOrganizacionalJPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    JOptionPane.showMessageDialog(null, "Caminho padrão configurado com sucesso.");
                    preencherCaminhoPlanoRisco(idProjetoSelecionado);
                } catch (Exception e) {
                    System.err.printf("Erro na configuração do caminho: %s. \n", e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_selecionarDiretorioPlanoRiscoJButtonActionPerformed


